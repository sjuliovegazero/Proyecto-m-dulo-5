        ax2.set_xlabel("Cantidad")
        ax2.set_ylabel("Total (€)")

        pdf.savefig(fig2)
        plt.show()
        plt.close(fig2)

        # Usamos un gráfico de barras que entregue las Ventas por Categoría y Región.
        # Para la personalización nos tocó averiguar acerca de las especificaciones.

        fig3 = plt.figure(figsize=(12, 6))
        sns.barplot(data=df, x="Categoria", y="Total", hue="Region", estimator=sum, errorbar=None)
