    g.fig.suptitle("Cantidad vs Total", fontsize=14)
    g.fig.subplots_adjust(top=0.95)
    plt.show()

    # Por otro lado Violinplot se utiliza para la evaluar la densidad de las ventas por sucursal.
    # Para la personalización nos tocó averiguar acerca de las especificaciones.

    plt.figure(figsize=(10, 5))
    sns.violinplot(data=df, x="Sucursal", y="Total", inner="quart", palette="Set2")
    plt.title("Densidad de Ventas por Sucursal")
    plt.show()

    # Así mismo FacetGrid se usa para hacer una segmentación por Región.
    # Para la personalización nos tocó averiguar acerca de las especificaciones.

    g = sns.FacetGrid(df, col="Region", col_wrap=3, height=4)
    g.map_dataframe(sns.scatterplot, x="Precio_Unitario", y="Total", alpha=0.6, color="#4C72B0")
    g.set_axis_labels("Precio Unitario (€)", "Total (€)")
    g.fig.suptitle("Relación Precio Unitario vs Total por Región", fontsize=16)
    g.fig.subplots_adjust(top=0.85)
    plt.show()


    print("\n--- Evaluación de posibles correlaciones espurias ---")

    from scipy.stats import pearsonr

    # Ejemplo: análisis de desempeño por Vendedor (variable categórica).

    plt.figure(figsize=(10,6))
    sns.boxplot(data=df, x="Vendedor", y="Total", palette="Set2")
    plt.title("Distribución del Total por Vendedor", fontsize=14)
    plt.xticks(rotation=45)
    plt.show()

    print("Se analiza Vendedor como variable categórica (correcto).")
    print("No se aplica correlación directa porque no es variable numérica continua.")

    # Ejemplo de correlación espuria (una relación NO válida).
    # Analizamos de forma específica estas variables, es por eso que debemos hacer los cálculos.

    print("\nSimulación de correlación espuria codificando Vendedor")

    df["Vendedor_cod"] = df["Vendedor"].astype("category").cat.codes

    r_spur, p_spur = pearsonr(df["Vendedor_cod"], df["Total"])

    print("Coeficiente de Pearson (Vendedor codificado vs Total):", r_spur)
    print("Valor p:", p_spur)

    print("\nInterpretación:")
    print("Esta correlación es ESPURIA.")
    print("El código numérico asignado al vendedor es arbitrario y no representa una magnitud económica.")
    print("Por lo tanto, no debe interpretarse como relación real.")

    # Eliminamos la columna auxiliar para no contaminar los análisis posteriores.
    df.drop(columns=["Vendedor_cod"], inplace=True)

    return df


