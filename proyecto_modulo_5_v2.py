# LECCIÓN 3 – CORRELACIÓN

def correlac_data(df):

    print("\n========== LECCIÓN 3 – CORRELACIÓN ==========")

    from scipy.stats import pearsonr

    # Decidimos convertir las variables a numéricas por seguridad de los cálculos.

    df["Cantidad"] = pd.to_numeric(df["Cantidad"], errors="coerce")
    df["Precio_Unitario"] = pd.to_numeric(df["Precio_Unitario"], errors="coerce")
    df["Total"] = pd.to_numeric(df["Total"], errors="coerce")

    # Excluimos ID_Venta para que no afecte la correlación

    numeric_df = df.select_dtypes(include=np.number).drop(columns=["ID_Venta"], errors="ignore")

    # Podemos crear una Matriz de correlación general que se represente por un heatmap:
    # Para la personalización nos tocó averiguar acerca de las especificaciones.

    plt.figure(figsize=(8,6))
    sns.heatmap(numeric_df.corr(), annot=True, cmap="coolwarm", linewidths=0.5)
    plt.title("Matriz de Correlación", fontsize=14)
    plt.show()

    # Aplicamos el coeficiente de Pearson para cuantificar la correlación entre variables:
    # Creamos dos cálculos para tener un mayor conocimiento del estado y así comparar.

    # Cantidad vs Total
    r1, p1 = pearsonr(df["Cantidad"], df["Total"])

    print("\nCantidad vs Total")
    print("Coeficiente de Pearson:", r1)
    print("Valor p:", p1)

    # Precio_Unitario vs Total
    r2, p2 = pearsonr(df["Precio_Unitario"], df["Total"])

    print("\nPrecio_Unitario vs Total")
    print("Coeficiente de Pearson:", r2)
    print("Valor p:", p2)

    # Interpretación automática de los datos obtenidos en esta sección:

    def interpretar(r, p, variable):
        if abs(r) > 0.7:
            fuerza = "fuerte"
        elif abs(r) > 0.4:
            fuerza = "moderada"
        else:
            fuerza = "débil"

        if p < 0.05:
            significancia = "Estadísticamente significativa"
        else:
            significancia = "Estadísticamente NO significativa"

        print(f"Correlación {fuerza} y {significancia} para {variable}")

    interpretar(r1, p1, "Cantidad")
    interpretar(r2, p2, "Precio_Unitario")

    return df


# LECCIÓN 4 – REGRESIONES LINEALES

def regres_lineal(df):

    print("\n========== LECCIÓN 4 – REGRESIONES LINEALES ==========")

    # Necesitaremos acudir a statsmodels para poder crear una regresión lineal
    # Así mismo llamaremos al cálculo de elementos fundamentales.

    import statsmodels.api as sm
    from sklearn.metrics import mean_squared_error, mean_absolute_error

    # Regresión simple (Cantidad - Total)

    X_simple = sm.add_constant(df["Cantidad"])
    y = df["Total"]

    model_simple = sm.OLS(y, X_simple).fit()

    print("\n--- Regresión Simple (Cantidad - Total) ---")
    print(model_simple.summary())

    # Acudimos a seaborn.regplot para generar una visualización de esta relación.
    # Para la personalización nos tocó averiguar acerca de las especificaciones.

    plt.figure(figsize=(8, 6))
    sns.regplot(
        x="Cantidad",
        y="Total",
        data=df,
        scatter_kws={"alpha": 0.5, "color": "#4C72B0"},
        line_kws={"color": "red", "linewidth": 2})
    plt.title("Regresión lineal: Cantidad vs Total", fontsize=14)
    plt.xlabel("Cantidad")
    plt.ylabel("Total (€)")
    plt.show()

    pred_simple = model_simple.predict(X_simple)

    # Podemos evaluar el modelo con los siguientes parámetros de prueba:

    print("MSE:", mean_squared_error(y, pred_simple))
    print("MAE:", mean_absolute_error(y, pred_simple))
    print("R2:", model_simple.rsquared)


    # Regresión múltiple (Cantidad - Precio_Unitario - Total)

    X_multiple = df[["Cantidad", "Precio_Unitario"]]
    X_multiple = sm.add_constant(X_multiple)

    model_multiple = sm.OLS(y, X_multiple).fit()

    print("\n--- Regresión Múltiple (Cantidad + Precio_Unitario → Total) ---")
    print(model_multiple.summary())
    print("R2 modelo múltiple:", model_multiple.rsquared)

    return df


