# LECCIÓN 6 – LIBRERÍA MATPLOTLIB

def matpl_datos(df):

    print("\n========== LECCIÓN 6 – LIBRERÍA MATPLOTLIB ==========")

    # Importamos PdfPages para guardar múltiples gráficos en un solo PDF.
    # Tuve que averiguar este elemento, ya que sin él, el documento saldría en blanco.

    from matplotlib.backends.backend_pdf import PdfPages

    with PdfPages("analisis_final_ventas.pdf") as pdf:

        # Generamos un histograma que resuma la frecuencia vs la totalidad de ventas.

        fig1, ax1 = plt.subplots(figsize=(8, 5))
        ax1.hist(df["Total"], bins=20, color="#55a868", edgecolor="black")
        ax1.set_title("Distribución del total de ventas")
        ax1.set_xlabel("Total (€)")
        ax1.set_ylabel("Frecuencia")

        pdf.savefig(fig1)
        plt.show()
        plt.close(fig1)

        # Entregamos un scatter que demuestre la relacion entre la Cantidad y el total.

        fig2, ax2 = plt.subplots(figsize=(8, 5))
        ax2.scatter(df["Cantidad"], df["Total"], alpha=0.6, color="#c44e52")
        ax2.set_title("Cantidad vs Total")
        ax2.set_xlabel("Cantidad")
        ax2.set_ylabel("Total (€)")

        pdf.savefig(fig2)
        plt.show()
        plt.close(fig2)

        # Decidi averiguar sobre otros gráficos que podría incluir para sumas al análisis:

        # Gráfico de barras que representa las Ventas por Categoría y Región (Segmentación)
        # para verificar geograficamente las diferencias.

        fig3 = plt.figure(figsize=(12, 6))
        sns.barplot(data=df, x="Categoria", y="Total", hue="Region", estimator=sum, errorbar=None)
        plt.title("Ventas Totales por Categoría y Región", fontsize=15)
        plt.ylabel("Euros Totales (€)")
        plt.legend(title="Región", bbox_to_anchor=(1.05, 1), loc='upper left')

        pdf.savefig(fig3)
        plt.show()
        plt.close(fig3)

        # Gráfico boxplot que mide el rendimiento por Vendedor para comparar las Ventas realizadas.

        fig4 = plt.figure(figsize=(10, 5))
        order = df.groupby("Vendedor")["Total"].sum().sort_values(ascending=False).index
        sns.boxplot(data=df, x="Vendedor", y="Total", order=order, palette="Set2")
        plt.title("Rendimiento y Consistencia de Vendedores", fontsize=14)

        pdf.savefig(fig4)
        plt.show()
        plt.close(fig4)

        # Gráfico que demuestra el Tipo de Cliente que acude a las diversas tiendas.

        fig5 = plt.figure(figsize=(8, 5))
        sns.countplot(data=df, x="Cliente", palette="pastel")
        plt.title("Distribución de Clientes (Nuevos vs Recurrentes)")

        pdf.savefig(fig5)
        plt.show()
        plt.close(fig5)

    print("PDF generado correctamente: analisis_final_ventas.pdf")

    return df


