        plt.ylabel("Euros Totales (€)")
        plt.legend(title="Región", bbox_to_anchor=(1.05, 1), loc='upper left')

        pdf.savefig(fig3)
        plt.show()
        plt.close(fig3)

        # Por medio de un gráfico boxplot medimos el rendimiento por Vendedor para comparar las ventas realizadas.
        # Para la personalización nos tocó averiguar acerca de las especificaciones.

        fig4 = plt.figure(figsize=(10, 5))
        order = df.groupby("Vendedor")["Total"].sum().sort_values(ascending=False).index
        sns.boxplot(data=df, x="Vendedor", y="Total", order=order, palette="Set2")
        plt.title("Rendimiento y Consistencia de Vendedores", fontsize=14)

        pdf.savefig(fig4)
        plt.show()
        plt.close(fig4)

        # Finalmente podemos hacer un análisis de Tipo de Cliente que acude a las tiendas.
